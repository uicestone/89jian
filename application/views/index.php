                <p>三、秋季。天高气爽，气候由热转凉，人体消化能力逐渐增强，食欲增加，宜食生津食品，膳食应有足够的热能。秋季各种动物肉肥味美，蔬菜瓜果品种齐全，在膳食调配上，只要注意种类的多样化，搭配得合理适当就可以了。在调味品上，可适当选用些辛辣品，如辣椒、胡椒等。由于秋季天气由热转凉，在饮食上应注意不要多食生冷的食物，并加强饮食卫生，预防肠道传染病。</p>
                <p>四、冬季。气候寒冷，膳食应有充足的热能，以抵御严寒。冬季是进补的佳季，可多吃些热性食物，如牛肉、羊肉、红枣、桂圆、板栗等；还可增加些厚味食品，如炖肉、火锅、油炸食品等，但不可过量，否则，会使血脂升高、血液偏酸性，对身体不利。此外，冬季蔬菜品种较少，北方多为储存菜，应特别注意多吃些绿叶菜、豆芽菜、萝卜等，以补充维生素的不足。调味品可多选些辛辣食物，如辣椒、胡椒、葱、姜、蒜等。</p>
